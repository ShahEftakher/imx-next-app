import type { NextPage } from 'next';
import Head from 'next/head';
import { useEffect, useState } from 'react';
import { ImmutableXClient, Link } from '@imtbl/imx-sdk';

const Home: NextPage = () => {
  const [client, setClient] = useState(Object);
  const [user, setUser] = useState({});
  const link = new Link('https://link.sandbox.x.immutable.com');

  const linkSetup = async () => {
    const res = await link.setup({});
    setUser(res);
  };

  const setupClient = async () => {
    const imxApiUrl = 'https://api.sandbox.x.immutable.com/v1';
    setClient(await ImmutableXClient.build({ publicApiUrl: imxApiUrl }));
  };

  useEffect(() => {
    setupClient();
  }, [client]);

  return (
    <div className="h-screen">
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="h-full flex justify-center items-center">
        <div className="h-screen flex flex-col justify-center items-center border-teal-600">
          <p className="">{JSON.stringify(user)}</p>
          <button
            className="border-4 p-2 bg-blue-600 border-red-400 rounded-lg"
            onClick={linkSetup}
          >
            Link Wallet
          </button>
        </div>
      </main>
    </div>
  );
};

export default Home;
